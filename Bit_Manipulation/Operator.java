package Bit_Manipulation;

public class Operator {
    public static void main(String[] args) {
        //And
        System.out.println(5 & 6);

        //OR
        System.out.println(5 | 6);

        //XOR
        System.out.println(5 ^ 6);

        //NOT(1's complement)
        System.out.println(~5);

        //Left Shift
        System.out.println(5 << 3);

        //Right Shift
        System.out.println(6 >> 1);
    }
}